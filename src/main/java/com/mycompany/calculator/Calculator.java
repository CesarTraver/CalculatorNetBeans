    private void manejarClicEnBotonOperador(String nuevoOperador) {
        // Obtener el número actual en el campo
        String textoActual = jTextField1.getText();
        
        // Convertir el texto a un número y sumarlo al resultado acumulado
        double numeroActual = Double.parseDouble(textoActual);
        resultado += numeroActual;

        // Actualizar el operador actual
        operador = nuevoOperador;

        // Borrar el texto en el campo
        jTextField1.setText("");
    }
    
    

