    private void jButtonEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEqualsActionPerformed
        // TODO add your handling code here:
        String textoActual = jTextField1.getText();
        String error = "Error";

        // Convertir el texto a un número y realizar la operación pendiente
        double numeroActual = Double.parseDouble(textoActual);
        if (operador.equals("+")) {
            resultado += numeroActual;
        } else if(operador.equals("-")) {
            resultado -= numeroActual;
        } else if(operador.equals("*")) {
            resultado *= numeroActual;
        } else if(operador.equals("/")) {
                resultado /= numeroActual;
        }

        // Mostrar el resultado en el campo
        jTextField1.setText(String.valueOf(resultado));

        // Reiniciar el resultado y el operador
        resultado = 0.0;
        operador = "";
    }//GEN-LAST:event_jButtonEqualsActionPerformed

    private void jButtonMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonMinusActionPerformed
        // TODO add your handling code here:
        String text = jTextField1.getText();

        if (!text.isEmpty()) {
            // Verificar si el número es negativo y cambiar el signo
            if (text.charAt(0) == '-') {
                // Si ya es negativo, realizar la operación de resta
                manejarClicEnBotonOperador("-");
            } else {
                // Si es positivo, cambiar el signo y mostrar en el campo
                jTextField1.setText("-" + text);
            }
        } else {
            // Si el campo está vacío, tratar como operador de resta y cambiar el signo
            manejarClicEnBotonOperador("-");
        }
    }//GEN-LAST:event_jButtonMinusActionPerformed

